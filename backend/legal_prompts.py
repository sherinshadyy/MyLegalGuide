def build_rag_user_prompt(
    question: str,
    contexts: list,
    chat_history=None,
    jurisdiction: str = "مصر",
) -> str:
    ctx_blocks = []
    for c in contexts:
        chunk_id = c.get("id", "unknown")
        src_ids = c.get("source_ids", [])
        content = c.get("content", "")
        if src_ids:
            ctx_blocks.append(f"[id={chunk_id}, sources={src_ids}]\n{content}")
        else:
            ctx_blocks.append(f"[id={chunk_id}]\n{content}")
    joined_ctx = "\n\n".join(ctx_blocks)

    history_str = ""
    if chat_history:
        recent = chat_history[-10:]
        parts = []
        for msg in recent:
            role = msg.get("role", "")
            if role == "user":
                parts.append(f"المستخدم: {msg['content']}")
            elif role == "assistant":
                parts.append(f"المساعد: {msg['content']}")
        if parts:
            history_str = "سجل المحادثة السابقة:\n" + "\n".join(parts) + "\n\n"

    return (
        f"الاختصاص القضائي المفترض: {jurisdiction}\n\n"
        f"{history_str}"
        f"السؤال الحالي: {question}\n\n"
        f"السياق المتاح:\n{joined_ctx}\n\n"
        "أجب بالهيكل المطلوب في تعليمات النظام (الأقسام الخمسة)."
    )


def build_document_user_prompt(document_text: str, user_note: str = "", jurisdiction: str = "مصر") -> str:
    note = f"\nملاحظة من المستخدم: {user_note}\n" if user_note.strip() else ""
    return (
        f"الاختصاص: {jurisdiction}\n"
        f"{note}\n"
        f"نص الوثيقة:\n---\n{document_text[:120000]}\n---\n\n"
        "لخّص الوثيقة بالهيكل الخمسة المطلوب."
    )


