def clean_answer(text: str) -> str:
    """Remove filler words and extra whitespace from answer."""
    if not isinstance(text, str):
        return ""
    text = text.strip()
    # Remove filler patterns from beginning
    for pat in FILLER_PATTERNS:
        text = re.sub(pat, "", text, count=1)
    # Remove any remaining leading punctuation like "،" or ","
    text = re.sub(r"^[,،]\s*", "", text)
    return text.strip()

# Apply cleaning to the 'answer' column
