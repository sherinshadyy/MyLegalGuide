def transform_existing_chunk(chunk):
    old_id = chunk.get("id", "")
    # Extract number part after "chunk-"
    match = re.search(r"chunk-(\d+)", old_id)
    if match:
        new_id = match.group(1)
    else:
        new_id = old_id                # fallback (should not happen)
    return {
        "chunked_id": new_id,
        "content": chunk["content"],
        "num_items": chunk.get("num_items", 1),
    }

