adjacency = defaultdict(set)

# Search for each item against top‑k candidates (k=50 is enough, most items have few neighbours)
k = min(50, len(embeddings))
for i, emb in enumerate(embeddings):
    scores, idxs = index.search(emb.reshape(1, -1).astype(np.float32), k)
    for j, score in zip(idxs[0], scores[0]):
        if i != j and score > threshold:
            adjacency[i].add(j)
            adjacency[j].add(i)

num_pairs = sum(len(v) for v in adjacency.values()) // 2
print(f"Found {num_pairs} similar pairs (undirected).")

# 4. Build connected components (groups of near‑duplicates)
# -------------------------------
def get_connected_components(adj, n):
    visited = [False] * n
    components = []
    for i in range(n):
        if not visited[i]:
            comp = []
            stack = [i]
            visited[i] = True
            while stack:
                node = stack.pop()
                comp.append(node)
                for nb in adj[node]:
                    if not visited[nb]:
                        visited[nb] = True
                        stack.append(nb)
            components.append(comp)
    return components

components = get_connected_components(adjacency, len(items))
