def is_legal_content(text: str) -> bool:
    if len(text) < 20:
        return False
    non_legal_patterns = [
        r"طبعه\s+\d{4}", r"مؤلف في سطور", r"المستشار أشرف مصطفى كمال",
        r"ليسانس الحقوق", r"نيابه القاهره", r"عضو هيئه المفوضين",
        r"صندوق الطوارئ", r"من ارشيف المؤلف", r"محتويات الكتاب",
        r"المذكره الايضاحيه", r"التعـــليق", r"احكام النقض",
    ]
    for pat in non_legal_patterns:
        if re.search(pat, text, re.IGNORECASE):
            return False
    legal_keywords = [
        "طلاق", "تطليق", "نفقة", "حضانة", "رؤية", "طاعة", "نشوز",
        "مهر", "صداق", "خلع", "عقوبة", "حبس", "غرامة", "إجراء",
        "حكم", "شرط", "استثناء", "نسب", "ميراث", "عدة", "ولاية"
    ]
    for kw in legal_keywords:
        if kw in text:
            return True
    return bool(re.search(r"[\u0600-\u06FF]{30,}", text))

def load_filtered_items(json_path: str):
    with open(json_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    items = []
    for d in data:
        if not isinstance(d, dict) or "id" not in d or "content" not in d:
            continue
        try:
            _id = int(d["id"])
        except:
            continue
        txt = str(d["content"]).strip()
        if txt and is_legal_content(txt):
            items.append((_id, txt))
    return items

